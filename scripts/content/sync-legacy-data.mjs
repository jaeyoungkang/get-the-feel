#!/usr/bin/env node

import { readFileSync, writeFileSync } from "node:fs";
import { join, resolve } from "node:path";

const ROOT = resolve(import.meta.dirname, "../..");
const CONTENT_DIR = join(ROOT, "assets", "content");
const OUT_PATH = join(ROOT, "public", "legacy", "c4-3", "data.js");
const ITEM_ORDER = [
  "have",
  "get",
  "take",
  "make",
  "keep",
  "be",
  "go",
  "come",
  "up",
  "out",
  "phrasal-up",
];

const bundle = {};
for (const itemKey of ITEM_ORDER) {
  bundle[itemKey] = JSON.parse(readFileSync(join(CONTENT_DIR, `${itemKey}.json`), "utf8"));
}

const output = [
  "// get-the-feel · c4-3 — 콘텐츠 임베드 (assets/content 11파일 verbatim, 손수정 금지)",
  "// Generated by scripts/content/sync-legacy-data.mjs. app.js보다 먼저 로드.",
  "// @promise promise:sense-training-surface",
  "// @aspect aspect:content-provenance",
  "// @aspect aspect:design-baseline-preservation",
  `window.CONTENT_ALL = ${JSON.stringify(bundle, null, 2)};`,
  "",
].join("\n");

writeFileSync(OUT_PATH, output, "utf8");
console.log(`synced ${OUT_PATH}`);
